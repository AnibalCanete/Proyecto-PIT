                <h1>Granulado</h1>
                <p>Este alimento es ideal para una amplia variedad de peces, tanto de agua dulce como agua salada. Su hundimiento lento permite que peces de todos los niveles del acuario puedan alimentarse sin dificultad.</p>
                <p>Cada gránulo está formulado con nutrientes esenciales, proporcionando a los peces una dieta equilibrada que favorece su salud, energía y vitalidad.</p>
                <p>La alimentación de los peces debe realizarse dos veces al día, ajustando la porción según la cantidad de peces en el acuario. Lo ideal es que consuman todo el alimento en un máximo de 2 a 3 minutos, evitando la sobrealimentación y manteniendo el agua limpia.</p>
                <p>El alimento granulado es especialmente recomendado para peces de agua fría, ya que suelen nadar más lentamente debido a su mayor tamaño. Dado que este tipo de alimento se hunde lentamente, les permite consumirlo con mayor facilidad sin generar desperdicios.</p>
            </div>

            <div id="Alimento Peces de Fondo" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/04 - Alimentación/03-Alimento Peces de Fondo.jpg" alt="Alimento Peces de Fondo">
                <h1>Alimento Peces de Fondo</h1>
                <p>Este alimento para peces de fondo es una opción completa y equilibrada para peces tropicales que habitan en la parte baja del acuario, como las corydoras. Su fórmula está diseñada para ser fácilmente digerible, permitiendo una absorción óptima de nutrientes sin enturbiar el agua.</p>
                <p>
                    Dosificación y recomendaciones: <br>
                    - Se recomienda administrar una pastilla cada día de por medio, por ejemplo, el lunes y luego el miércoles. <br>
                    - Es importante considerar que otros peces tropicales pueden intentar consumir este alimento. Para evitarlo, se recomienda alimentar a los demás peces en un extremo del acuario y colocar la pastilla en el lado opuesto, permitiendo que llegue al fondo sin ser ingerida por peces de otras especies. 
                </p>
                <p>Este método garantiza que los peces de fondo reciban la nutrición adecuada, manteniendo un acuario limpio y equilibrado.</p>

                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/01-Calefactor.jpg" alt="Calefactor">
                <h1>Calefactor</h1>
                <p>Es un dispositivo esencial diseñado para mantener la temperatura adecuada en los acuarios. La potencia recomendada para un calefactor debe corresponder al volumen de agua del acuario, siguiendo la regla general de 1 watt por cada litro de agua. Por ejemplo, un calefactor de 25 watts es ideal para un acuario de 25 litros.</p>
                <p>En algunos casos, es más eficiente utilizar dos calefactores de media potencia en lugar de uno de mayor potencia. Esto ayuda a distribuir el calor de manera más uniforme y reduce el riesgo de sobrecalentamiento. Por ejemplo, en un acuario de 50 litros, se pueden instalar dos calefactores de 25 watts en lugar de un solo calefactor de 50 watts.</p>
                <p>Para garantizar el uso seguro y eficiente de un calefactor en el acuario, es fundamental seguir ciertas recomendaciones: <br>
                    - Apaga el calefactor al menos 30 minutos antes de cada cambio de agua para evitar daños por diferencias bruscas de temperatura. <br>
                    - Colócalo en un lugar visible dentro del acuario para facilitar su monitoreo. <br>
                    - Asegura una buena circulación del agua para distribuir el calor de manera uniforme y prevenir el sobrecalentamiento. <br>
                    - Nunca introduzcas la mano en el acuario mientras el calefactor esté enchufado, ya que podría generar riesgos eléctricos. <br>
                    - Revisa periódicamente que el calefactor no esté roto y que conserve sus protectores en buen estado. <br>
                    - Asegúrate de que esté completamente sumergido en el agua para su correcto funcionamiento. <br>
                </p>
            </div>

            <div id="Filtro" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/02-Filtro.jpg" alt="Filtro">
                <h1>Filtro</h1>
                <p>El mantenimiento del filtro debe realizarse una vez por semana para garantizar su correcto funcionamiento. Para ello, sigue estos pasos: <br>
                    - Desenchufa el filtro antes de comenzar el proceso de limpieza. <br>
                    - Retíralo del acuario y desármalo con cuidado. <br>
                    - Límpialo con un cepillo exclusivo para el acuario y con agua de la canilla, evitando el uso de detergentes o productos químicos. <br>
                    - Lava la esponja del filtro, considerando que su vida útil es de aproximadamente 6 meses. Después de este tiempo, reemplázala por una nueva. <br>
                    - Limpia el rotor del filtro, asegurándote de no perder las gomitas que lo sostienen, ya que son esenciales para su estabilidad. <br>
                    - Una vez finalizada la limpieza, rearma el filtro y colócalo nuevamente en el acuario. <br>
                    - Este procedimiento ayuda a mantener una filtración eficiente y un entorno saludable para los peces. <br>
                </p>
                <p>Para garantizar el correcto funcionamiento del filtro, es fundamental que esté siempre completamente sumergido en el agua. Si el filtro queda fuera del agua, el rotor puede sobrecalentarse, lo que podría deformar sus bases y hacer que deje de funcionar.</p>
                <p>En casos de baja tensión, existe el riesgo de que el filtro se queme. Ante esta situación, se recomienda desenchufarlo de inmediato y utilizar un aireador a pila para mantener la oxigenación del acuario hasta que el suministro eléctrico se restablezca.</p>
            </div>

            <div id="Sifon" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/03-Sifon.jpg" alt="Sifón">
                <h1>Sifón</h1>
                <p>El sifón es una herramienta esencial en el mantenimiento del acuario, funcionando como una aspiradora que elimina el agua mediante el efecto de gravedad. Su uso es fundamental para remover restos de comida y heces de los peces, ayudando a mantener un entorno limpio y saludable.</p>
                <p>
                    Cómo utilizar el sifón correctamente: <br>
                    - Coloca un recipiente a una altura inferior al acuario para recolectar el agua sucia. <br>
                    - Introduce el sifón en el agua desde la parte superior del acuario. <br>
                    - Presiona la bomba del sifón tres veces hasta que comience a absorber el agua. <br>
                    - Una vez que el sifón esté funcionando, desplázalo por el fondo del acuario para succionar la suciedad acumulada. <br>
                    - Retira el sifón cuando hayas eliminado aproximadamente el 25% del agua del acuario. <br>
                </p>
                <p>
                    Existen varios tipos de sifón, cada uno diseñado para facilitar la limpieza del acuario: <br>
                    - Sifón básico: Cuenta con una boquilla estándar que evita la succión accidental de los peces. <br>
                    - Sifón para grava: Incluye una boquilla especial que permite absorber y limpiar la grava, eliminando los desechos acumulados sin remover el sustrato por completo. <br>
                    - Sifón eléctrico: Es la opción más avanzada, ya que incorpora varias herramientas para un sifonado eficiente. Incluye: <br>
                    - Boquillas intercambiables para evitar succionar peces y para limpiar la grava. <br>
                    - Un motor que permite la absorción automática del agua, facilitando el proceso. <br>
                    - Ventosas para fijarlo al vidrio del acuario y permitir un drenaje cómodo y seguro. <br>
                </p>
            </div>

            <div id="Anticloro" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/04-Anticloro.jpg" alt="Anticloro">
                <h1>Anticloro</h1>
                <p>El anticloro es un producto esencial para eliminar el cloro del agua antes de incorporarla al acuario. Esto es fundamental, ya que el cloro puede actuar como un agente tóxico para los peces, afectando su salud y poniendo en riesgo su vida. La presencia de cloro en el agua es comparable a verter un líquido corrosivo en el ecosistema del acuario, lo que puede desestabilizar el equilibrio biológico y provocar enfermedades graves en los peces.</p>
                <p>Además, el cloro puede dañar las plantas naturales, quemando sus tejidos hasta secarlas por completo. Por ello, el uso de anticloro es indispensable para mantener un ambiente seguro y saludable en el acuario.</p>
                <p>
                    Existen varios tipos de anticloro, cada uno con una dosificación específica según el volumen del acuario: <br>
                    - Anticloro común: Su dosificación estándar es de 1 tapita por cada 5 litros de agua. <br>
                    - Anticloros especializados: Estos productos cuentan con una dosificación propia, que puede variar dependiendo del tamaño del acuario y la concentración de cloro presente en el agua.
                </p>
                <p>Es importante seguir siempre las indicaciones del fabricante para garantizar una correcta neutralización del cloro y mantener un ambiente seguro para los peces y plantas.</p>
            </div>

            <div id="Cambio de Agua" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/05-Cambio de Agua.jpg" alt="Cambio de Agua">
                <h1>Cambio de Agua</h1>
                <p>El cambio de agua es uno de los procesos más importantes en el mantenimiento del acuario, ya que se debe realizar una vez por semana, junto con la limpieza del filtro. Para mantener el equilibrio del ecosistema, el cambio de agua debe ser de entre 20% y 25% del volumen total del acuario, evitando reemplazar la totalidad del agua.</p>
                <p>El proceso se realiza sin necesidad de sacar a los peces del acuario, asegurando que no sufran estrés innecesario. Si utilizas agua de la canilla, es imprescindible agregar anticloro antes de reponerla. Lo ideal es preparar el agua con un día de anticipación, pero si la situación lo requiere, se debe agregar anticloro al menos una hora antes de efectuar el cambio.</p>
                <p>Cuando se utiliza agua mineral para reponer el agua del acuario, el proceso es directo, ya que este tipo de agua está adaptada para el consumo humano. Esto significa que no contiene cloro y mantiene un pH estable, lo que la hace segura para los peces y ayuda a preservar el equilibrio del ecosistema acuático.</p>
            </div>

            <div id="Iluminacion" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/06-Iluminacion.jpg" alt="Iluminación">
                <h1>Iluminación</h1>
                <p>El uso de iluminación en el acuario debe limitarse a un máximo de 5 a 6 horas por día. Esto es importante porque el exceso de luz favorece la proliferación de algas, lo que, aunque no afecta la salud de los peces, puede volver el agua verde y generar una apariencia poco estética.</p>
                <p>Para eliminar o prevenir la formación de algas en el acuario, se recomienda el uso de un antialgas, un producto diseñado para controlar su proliferación. Además, este tratamiento debe complementarse con un clarificante, que ayuda a restaurar la transparencia del agua, mejorando la estética del acuario y manteniendo un entorno limpio para los peces.</p>
            </div>

            <div id="PH" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/03%20-%20Mantenimiento/07-PH.jpg" alt="PH">
                <h1>PH</h1>
                <p>Realizar pruebas de pH en cada mantenimiento del acuario es fundamental para conocer el estado del agua y garantizar un ambiente saludable para los peces. El rango ideal de pH debe mantenerse entre 6.5 y 7.5, ya que valores fuera de este intervalo pueden afectar el equilibrio del ecosistema.</p>
                <p>Si el pH es demasiado alto, se recomienda utilizar un acidificante para reducirlo. Por el contrario, si el pH es demasiado bajo, es necesario aplicar un alcalinizante para elevarlo y estabilizar las condiciones del agua.</p>
                <p>
                    Al adquirir peces, es fundamental realizar un proceso de adaptación antes de incorporarlos al acuario. Para ello, sigue estos pasos: <br>
                    - Deja flotando la bolsa con el pez dentro del acuario durante 15 minutos. Esto permite que la temperatura del agua de la bolsa se iguale a la del acuario, evitando cambios bruscos que podrían afectar la salud del pez. <br>
                    - Pasado el tiempo indicado, realiza un corte en la bolsa para que el agua de la bolsa comience a mezclarse con la del acuario. Esto ayuda a regular el pH y a que el pez se adapte mejor a su nuevo entorno. <br>
                    - Después de unos minutos, permite que el pez salga de la bolsa de forma natural, evitando forzarlo o generar estrés innecesario.
                </p>
                <p>Este procedimiento asegura una transición segura y reduce el riesgo de shock térmico y estrés, favoreciendo la adaptación del pez a su nuevo hogar.</p>

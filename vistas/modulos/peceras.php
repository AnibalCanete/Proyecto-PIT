                <h1>Iluminación</h1>
                <p>La iluminación del acuario debe elegirse en función de su longitud. Por ejemplo, si la pecera mide 25 cm de largo, una luz de 20 cm podría ajustarse bien, proporcionando una iluminación adecuada para el ecosistema.</p>
                <p>La iluminación del acuario puede ser multicolor o de un solo tono, como blanco, rosado o azul. En particular, una luz azul resalta las decoraciones fluorescentes, creando un efecto visual llamativo y mejorando la estética del entorno.</p>
            </div>

            <div id="Filtro" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/01%20-%20Peceras/04-Filtro.jpg" alt="Filtro">
                <h1>Filtro</h1>
                <p>El filtro es el corazón del acuario, ya que mantiene el ecosistema saludable al mover el agua, facilitando su oxigenación. Además, elimina la suciedad y las impurezas, asegurando un ambiente limpio y equilibrado para los peces.</p>
                <p>El filtro debe garantizar una circulación adecuada del agua en el acuario, moviéndola al menos de 3 a 4 veces por hora. Es esencial elegir un filtro acorde al litraje del acuario, ya que uno demasiado potente puede generar una corriente excesiva que estresaría a los peces. Por el contrario, si el filtro es demasiado débil, la filtración será insuficiente, acumulando suciedad y afectando el equilibrio del ecosistema.</p>
            </div>

            <div id="Decoracion" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/01%20-%20Peceras/05-Decoración.jpg" alt="Decoración">
                <h1>Decoración</h1>
                <p>La decoración del acuario es clave para crear un ambiente atractivo y armonioso. Puede incluir elementos como plantas artificiales, piedritas de colores, rocas y adornos temáticos como barcos o submarinos. También existen decoraciones fluorescentes, como plantas, piedras, caballitos de mar y medusas, que resaltan bajo una iluminación adecuada, aportando un efecto visual llamativo.</p>
                <p>Además de embellecer el acuario, la decoración también ofrece refugios para los peces. Por ejemplo, las plantas decorativas pueden simular un entorno natural, permitiendo que los peces naden entre ellas y se resguarden, lo que contribuye a su bienestar y sensación de seguridad.</p>
            </div>


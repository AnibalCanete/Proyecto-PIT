                <h1>Grava</h1>
                <p>La grava es un sustrato ideal para el acuario, ya que permite colocar decoraciones como plantas artificiales, piedras y también sirve para plantar vegetación natural. Es recomendable optar por una grava compacta, ya que esto evita que la suciedad que se deposita en el fondo se mezcle con el sustrato y facilita su absorción por el filtro, contribuyendo a un ecosistema más limpio y eficiente.</p>

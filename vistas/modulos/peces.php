                <h1>Corydora</h1>
                <p>Este pez necesita vivir en grupos, lo que lo convierte en una excelente opción para acuarios comunitarios. Contribuye a mantener el fondo limpio, aunque no se alimenta de desechos ni excrementos. Como omnívoro, requiere una alimentación variada para asegurar su bienestar. Las corydoras se sienten cómodas en un rango de temperatura de 22° a 26°C.</p>
                <p>Para asegurar un entorno óptimo, se recomienda que el acuario tenga abundante vegetación y un sustrato de arena o grava suave. Esto no solo recrea su hábitat natural, sino que también mejora su comodidad y bienestar, favoreciendo su desarrollo y adaptación. Algunas variedades incluyen la corydora panda, paleatus y aeneus, entre otras.</p>

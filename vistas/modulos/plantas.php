                <h1>Azolla</h1>
                <p>La Azolla, también conocida como helecho de agua, es un género de helechos acuáticos que flotan libremente en la superficie del agua. Originaria de diversas regiones climáticas y altitudes, se caracteriza por su rápida propagación y gran adaptabilidad, lo que la convierte en una opción popular para acuarios y estanques.</p>
                <p>
                    Beneficios ecológicos y agrícolas: <br>
                    - Su capacidad de crecimiento acelerado la hace ideal para la recuperación de ecosistemas acuáticos. <br>
                    - Es un suplemento valioso en la alimentación animal, proporcionando una fuente de nutrientes rica y sostenible. <br>
                    - Se ha utilizado como fertilizante orgánico, enriqueciendo el suelo con nutrientes esenciales y mejorando la salud y rendimiento de las plantas cultivadas.
                </p>
                <p>Gracias a su versatilidad y beneficios, la Azolla es una planta de gran interés tanto en el ámbito ecológico como agrícola.</p>

            <div id="Cabomba Caroliniana" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/05%20-%20Plantas/03-Cambomba Caroliniana.jpg" alt="Cabomba Caroliniana">
                <h1>Cabomba Caroliniana</h1>
                <p>La Cabomba Caroliniana es una planta acuática de tallo delicado, muy popular en acuarios debido a su atractivo follaje. Sus hojas recortadas en filamentos de color verde se distribuyen uniformemente a lo largo del tallo, formando un penacho de hojas nuevas en la parte superior.</p>
                <p>
                    Cuidados y mantenimiento: <br>
                    - Para garantizar un correcto enraizamiento, se recomienda enterrar horizontalmente un trozo de tallo sin hojas de 5 a 10 cm en un sustrato abonado con turba. <br>
                    - Esta planta también puede crecer en estado flotante durante largos períodos, permitiendo observar las raíces que emergen en los nudos. <br>
                    - Es importante separar los tallos enraizados dejando entre ellos una distancia de 2 a 3 cm, evitando la competencia entre raíces y asegurando que la luz llegue correctamente a la parte baja de todas las plantas. <br>
                    - Se recomienda evitar colocarla cerca de plantas de rápido crecimiento, ya que podrían generar sombra y frenar su desarrollo.
                </p>
                <p>
                    Poda y propagación: <br>
                    - La poda debe realizarse cuando la planta alcanza la superficie del acuario. <br>
                    - En ese momento, se puede cortar la parte superior de la planta, siempre entre dos nudos. <br>
                    - La cabeza cortada puede ser replantada, mientras que del tallo podado solo deben quedar dos nudos para favorecer su regeneración.
                </p>

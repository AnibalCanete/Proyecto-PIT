            <div id="Sustrato Nutritivo" class="Descripcion">
                <img src="/Proyecto-PIT/assets/imagenes/05%20-%20Plantas/05-Sustrato Nutritivo.jpg" alt="Sustrato Nutritivo">
                <h1>Sustrato Nutritivo</h1>
                <p>Este sustrato enriquecido contiene minerales y oligoelementos esenciales que favorecen un crecimiento vigoroso y saludable de las plantas acuáticas. Su composición equilibrada garantiza la seguridad y bienestar de los peces, sin alterar la calidad del agua.</p>
                <p>
                    Modo de uso: <br>
                    - Preparación: Lava el sustrato ligeramente para eliminar cualquier residuo o polvo suelto. <br>
                    - Distribución: Extiende el sustrato de manera uniforme en el fondo del acuario, asegurando una profundidad de 5 a 7 centímetros. <br>
                    - Instalación: Utiliza pinzas para enterrar las plantas en el sustrato, facilitando su enraizamiento. <br>
                    - Mantenimiento: Controla los parámetros del agua según las necesidades de las plantas y peces, asegurando un entorno óptimo para su desarrollo.
                </p>
                <p>Este procedimiento garantiza un acuario saludable y equilibrado, promoviendo el crecimiento adecuado de las plantas y el bienestar de los peces.</p>
